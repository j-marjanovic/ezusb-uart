#define NULL        (void*)0;

/* High and Low byte of a word (uint16_t) */
#define HI8(word)   (uint8_t)(((uint16_t)word >> 8) & 0xff)
#define LO8(word)   (uint8_t)((uint16_t)word & 0xff)


